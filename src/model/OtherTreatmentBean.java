package model;

public class OtherTreatmentBean {
	private String bisphosphonates;
	private String radiotherapy;
	private boolean dialysis;
	private String otherMedications;
	private String complications;
	
	public String getBisphosphonates() {
		return bisphosphonates;
	}
	public void setBisphosphonates(String bisphosphonates) {
		this.bisphosphonates = bisphosphonates;
	}
	public String getRadiotherapy() {
		return radiotherapy;
	}
	public void setRadiotherapy(String radiotherapy) {
		this.radiotherapy = radiotherapy;
	}
	public boolean isDialysis() {
		return dialysis;
	}
	public void setDialysis(boolean dialysis) {
		this.dialysis = dialysis;
	}
	public String getOtherMedications() {
		return otherMedications;
	}
	public void setOtherMedications(String otherMedications) {
		this.otherMedications = otherMedications;
	}
	public String getComplications() {
		return complications;
	}
	public void setComplications(String complications) {
		this.complications = complications;
	}
	
	
}
